-- Очистка
DROP TABLE IF EXISTS exhibit_category_links CASCADE;
DROP TABLE IF EXISTS exhibits CASCADE;
DROP TABLE IF EXISTS exhibitions CASCADE;
DROP TABLE IF EXISTS event_registrations CASCADE;
DROP TABLE IF EXISTS events CASCADE;
DROP TABLE IF EXISTS reviews CASCADE;
DROP TABLE IF EXISTS search_logs CASCADE;
DROP TABLE IF EXISTS staff CASCADE;
DROP TABLE IF EXISTS museums CASCADE;
DROP TABLE IF EXISTS exhibit_categories CASCADE;
DROP TYPE IF EXISTS user_role CASCADE;

-- 1. Музеи
CREATE TABLE museums (
    id SERIAL PRIMARY KEY,
    name TEXT NOT NULL,
    short_name TEXT,
    description TEXT,
    address TEXT,
    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    opening_hours TEXT,
    phone TEXT,
    email TEXT,
    website TEXT,
    image_url TEXT
);

-- 2. Выставки (принадлежат музею)
CREATE TABLE exhibitions (
    id SERIAL PRIMARY KEY,
    museum_id INTEGER REFERENCES museums(id) ON DELETE CASCADE,
    title TEXT NOT NULL,
    description TEXT,
    image_url TEXT,
    start_date DATE,
    end_date DATE,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 3. Экспонаты (принадлежат выставке)
CREATE TABLE exhibits (
    id SERIAL PRIMARY KEY,
    exhibition_id INTEGER REFERENCES exhibitions(id) ON DELETE CASCADE,
    title TEXT NOT NULL,
    description TEXT,
    image_url TEXT,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 4. Категории экспонатов
CREATE TABLE exhibit_categories (
    id SERIAL PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    description TEXT
);

-- 5. Связь экспонатов с категориями
CREATE TABLE exhibit_category_links (
    exhibit_id INTEGER REFERENCES exhibits(id) ON DELETE CASCADE,
    category_id INTEGER REFERENCES exhibit_categories(id) ON DELETE CASCADE,
    PRIMARY KEY (exhibit_id, category_id)
);

-- 6. Мероприятия
CREATE TABLE events (
    id SERIAL PRIMARY KEY,
    museum_id INTEGER REFERENCES museums(id) ON DELETE CASCADE,
    title TEXT NOT NULL,
    description TEXT,
    event_date TIMESTAMP NOT NULL,
    duration_hours INTEGER DEFAULT 2,
    max_participants INTEGER,
    current_participants INTEGER DEFAULT 0,
    price DECIMAL(10,2) DEFAULT 0,
    image_url TEXT,
    is_active BOOLEAN DEFAULT true
);

-- 7. Записи на мероприятия
CREATE TABLE event_registrations (
    id SERIAL PRIMARY KEY,
    event_id INTEGER REFERENCES events(id) ON DELETE CASCADE,
    user_id BIGINT NOT NULL,
    chat_id BIGINT NOT NULL,
    remind_hours INTEGER NOT NULL DEFAULT 4 CHECK (remind_hours IN (4, 12, 24)),
    reminded BOOLEAN DEFAULT false,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (event_id, user_id)
);

-- 8. Отзывы
CREATE TABLE reviews (
    id SERIAL PRIMARY KEY,
    museum_id INTEGER REFERENCES museums(id) ON DELETE CASCADE,
    user_id BIGINT NOT NULL,
    rating INTEGER CHECK (rating >= 1 AND rating <= 5),
    comment TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (museum_id, user_id)
);

-- 9. Поисковые запросы
CREATE TABLE search_logs (
    id SERIAL PRIMARY KEY,
    user_id BIGINT,
    query TEXT NOT NULL,
    results_count INTEGER DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 10. Сотрудники
CREATE TYPE user_role AS ENUM ('bot_admin', 'museum_admin', 'content_manager', 'analyst');

CREATE TABLE staff (
    id SERIAL PRIMARY KEY,
    user_id BIGINT UNIQUE NOT NULL,
    email TEXT NOT NULL,
    role user_role NOT NULL,
    museum_id INTEGER REFERENCES museums(id) ON DELETE SET NULL,
    alias TEXT,
    current_otp VARCHAR(6),
    otp_expires_at TIMESTAMP,
    is_active BOOLEAN DEFAULT true,
    last_login TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

